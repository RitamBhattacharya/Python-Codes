# Write a short program to input a digit and print it in words.

d=int(input("Enter a digit"))
if d==1:
    print("ONE")
elif d==2:
    print("TWO")
elif d==3:
    print("THREE")
elif d==4:
    print("FOUR")
elif d==5:
    print("FIVE")
elif d==6:
    print("SIX")
elif d==7:
    print("SEVEN")
elif d==8:
    print("EIGHT")
elif d==9:
    print("NINE")
elif d==0:
    print("ZERO")
else:
    print("Enter a right digit")                